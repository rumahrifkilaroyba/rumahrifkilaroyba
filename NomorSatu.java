public class NomorSatu {
     public final int MAX_VALUE = 100;
     public final void printhello(){
          System.out.println("Hallo word");
     }
     public static void main(String[] args) {
          NomorSatu ns = new NomorSatu();
          System.out.println("Max value:"+ ns.MAX_VALUE);
          ns.printhello();
     }
}
